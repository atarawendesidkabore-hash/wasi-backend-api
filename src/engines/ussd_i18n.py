def t(key: str, lang: str = "fr") -> str:
    """Look up a translated string.

    Fallback chain: lang → fr → raw key.
    """
    if lang in STRINGS and key in STRINGS[lang]:
        return STRINGS[lang][key]
    # Fallback to French
    if key in STRINGS.get("fr", {}):
        return STRINGS["fr"][key]
    # Last resort: return the key itself
    return key


def detect_lang(country_code: str) -> str:
    """Return the default USSD language for a country."""
    return COUNTRY_DEFAULT_LANG.get(country_code, "fr")
